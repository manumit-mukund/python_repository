import numpy as np

# Creating a 1D array
arr1 = np.array([1, 2, 3, 4, 5])
print("1D Array:", arr1)

# Creating a 2D array`
arr2 = np.array([[1, 2, 3], [4, 5, 6]])
print("\n2D Array:\n", arr2)

# Accessing elements
print("\nElement at index 0 of arr1:", arr1[0])
print("\nElement at row 1, column 2 of arr2:", arr2[1, 2])